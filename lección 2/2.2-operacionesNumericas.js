//JavaScript usa el símbolo "+" como operador de suma cuando se coloca entre dos números.

const edad = 5 + 10;

// JavaScript usa el símbolo "-"" para restar.

const manzanas = 44 - 6;

// JavaScript usa el símbolo "*"" para la multiplicación de dos números.

const personas = 11 * 13;

// JavaScript usa el símbolo "/" para la división.

const dinero = 16 / 2;

// Puede sumarle 1 a una variable con el operador ++.

let i = 1;
i++;

// es el equivalente de

i = i + 1;

// Puede restarle 1 a una variable con el operador --

let j = 10;
j--;

// Es el equivalente de

j = j - 1;

